// applyFileClassification applies smart truncation and warnings based on file
// size classification. The prefix is prepended to the result (e.g. cache note).
func applyFileClassification(content string, lineCount, dataLen int, prefix string) string {
	classification := ClassifyFileSize(lineCount, dataLen)
	switch classification.Action {
	case "truncate":
		headLines := largeFileLineThreshold * 3 / 4
		tailLines := largeFileLineThreshold / 4
		return prefix + classification.Warning + "\n" + TruncateToLines(content, headLines, tailLines)
	case "warn":
		return prefix + classification.Warning + "\n" + content
	default:
		return prefix + content
	}
}

func readTool(workingDir string) func(ctx context.Context, rawArgs []byte) (string, error) {
	type args struct {
		Path   string `json:"path"`
		Offset int    `json:"offset,omitempty"` // 1-based start line
		Limit  int    `json:"limit,omitempty"`  // max lines
	}
	return func(ctx context.Context, rawArgs []byte) (string, error) {
		var payload args
		if err := json.Unmarshal(rawArgs, &payload); err != nil {
			return "", fmt.Errorf("invalid args: %w", err)
		}

		// Hard-block reads only when the edit deadline is approaching.
		if e := GetEditEnforcer(ctx); e != nil && e.ShouldBlockReads() {
			logging.InfoContext(ctx, "blocking Read call — edit deadline approaching, no edits made yet")
			return "READ BLOCKED — You have already read files. The Read tool is disabled until you call Edit or Write.\n\n" +
				"You are approaching the edit deadline with no edits made. Use the file contents from your " +
				"previous Read calls to construct Edit calls now. Do NOT call Read, Glob, or Grep until you " +
				"have made at least one Edit or Write call.", nil
		}

		path, err := ResolvePath(workingDir, payload.Path)
		if err != nil {
			return "", err
		}
		data, err := os.ReadFile(path)
		if err != nil {
			return "", err
		}

		// Record that this file was read (for edit safety checks).
		if ft := GetFileTracker(ctx); ft != nil {
			ft.RecordRead(path)
		}

		// If using offset/limit, skip cache and smart truncation — targeted read.
		if payload.Offset > 0 || payload.Limit > 0 {
			return sliceLines(data, payload.Offset, payload.Limit), nil
		}

		lineCount := strings.Count(string(data), "\n") + 1

		// Check read cache for duplicate reads.
		// Before any edits: return full content with a warning so the model
		// still has data to work from (compaction may have removed the original).
		// After first edit: return a short stub to prevent wasteful re-reads.
		if rc := GetReadCache(ctx); rc != nil {
			hash := HashContent(data)
			if entry, hit := rc.Check(path, hash); hit {
				logReadCacheHit(ctx, payload.Path, entry)
				preEdit := GetEditEnforcer(ctx).PreEdit()
				if preEdit {
					// Return full content with warning — model needs it to write edits.
					// Escalate warning based on how many phase nudges have been ignored.
					var prefix string
					if pe := GetPhaseEnforcer(ctx); pe != nil && pe.NudgesSent() >= 2 {
						prefix = fmt.Sprintf(
							"[WARNING — REPEATED RE-READ #%d. You already read this file at iteration %d. "+
								"Your next tool call MUST be Edit or Write, not Read. "+
								"Here is the content — use it to write tests NOW.]\n",
							pe.NudgesSent(), entry.Iteration)
					} else {
						prefix = fmt.Sprintf(
							"[CACHED — you already read this file at iteration %d. "+
								"STOP re-reading and start writing Edit calls NOW.]\n",
							entry.Iteration)
					}
					return applyFileClassification(string(data), lineCount, len(data), prefix), nil
				}
				return fmt.Sprintf(
					"[CACHED — file unchanged since iteration %d (%d lines, %d bytes). "+
						"You already read this file. Use the contents from your earlier read "+
						"to write Edit calls NOW. Do NOT re-read this file.]",
					entry.Iteration, entry.Lines, entry.Bytes), nil
			}
			rc.Store(path, hash, lineCount, len(data), GetIteration(ctx))
		}

		// Smart truncation for large files.
		if len(data) > maxReadBytes {
			return truncateHeadTail(data), nil
		}

		return applyFileClassification(string(data), lineCount, len(data), ""), nil
	}
}

func sliceLines(data []byte, offset, limit int) string {
	lines := strings.SplitAfter(string(data), "\n")
	totalLines := len(lines)
	// Trim trailing empty element from SplitAfter
	if totalLines > 0 && lines[totalLines-1] == "" {
		lines = lines[:totalLines-1]
		totalLines = len(lines)
	}
	if offset < 1 {
		offset = 1
	}
	start := offset - 1
	if start >= totalLines {
		return fmt.Sprintf("(file has %d lines, offset %d is past end)", totalLines, offset)
	}
	end := totalLines
	if limit > 0 && start+limit < end {
		end = start + limit
	}
	var sb strings.Builder
	fmt.Fprintf(&sb, "[lines %d-%d of %d]\n", offset, end, totalLines)
	for _, line := range lines[start:end] {
		sb.WriteString(line)
	}
	return sb.String()
}

func truncateHeadTail(data []byte) string {
	totalLines := strings.Count(string(data), "\n") + 1
	headSize := maxReadBytes * 3 / 4
	tailSize := maxReadBytes - headSize

	head := string(data[:headSize])
	tail := string(data[len(data)-tailSize:])

	// Find clean line boundaries
	headEnd := strings.LastIndex(head, "\n")
	if headEnd < 0 {
		headEnd = len(head)
	}
	tailStart := strings.Index(tail, "\n")
	if tailStart < 0 {
		tailStart = 0
	} else {
		tailStart++ // skip the newline itself
	}

	headLines := strings.Count(head[:headEnd], "\n") + 1
	tailLines := strings.Count(tail[tailStart:], "\n") + 1
	omitted := totalLines - headLines - tailLines

	return fmt.Sprintf("%s\n\n... [%d lines omitted — file has %d total lines, %d bytes. Use Read with offset/limit for specific sections.] ...\n\n%s",
		head[:headEnd], omitted, totalLines, len(data), tail[tailStart:])
}

func writeTool(workingDir string) func(ctx context.Context, rawArgs []byte) (string, error) {
	type args struct {
		Path    string `json:"path"`
		Content string `json:"content"`
	}
	return func(_ context.Context, rawArgs []byte) (string, error) {
		var payload args
		if err := json.Unmarshal(rawArgs, &payload); err != nil {
			return "", fmt.Errorf("invalid args: %w", err)
		}
		path, err := ResolvePath(workingDir, payload.Path)
		if err != nil {
			return "", err
		}
		if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
			return "", err
		}
		if err := os.WriteFile(path, []byte(payload.Content), 0o644); err != nil {
			return "", err
		}
		return fmt.Sprintf("wrote %s (%d bytes)", filepath.ToSlash(payload.Path), len(payload.Content)), nil
	}
}

func editTool(workingDir string) func(ctx context.Context, rawArgs []byte) (string, error) {
	type args struct {
		Path       string   `json:"path"`
		Old        string   `json:"old"`
		New        string   `json:"new"`
		ReplaceAll FlexBool `json:"replace_all"`
	}
	return func(ctx context.Context, rawArgs []byte) (string, error) {
		var payload args
		if err := json.Unmarshal(rawArgs, &payload); err != nil {
			return "", fmt.Errorf("invalid args: %w", err)
		}
		path, err := ResolvePath(workingDir, payload.Path)
		if err != nil {
			return "", err
		}
		if ft := GetFileTracker(ctx); ft != nil {
			if err := ft.ValidateBeforeEdit(path); err != nil {
				return "", err
			}
		}
		data, err := os.ReadFile(path)
		if err != nil {
			return "", err
		}
		content := string(data)
		if !strings.Contains(content, payload.Old) {
			return "", fmt.Errorf("text not found in %s", payload.Path)
		}
		var updated string
		replaced := 1
		if bool(payload.ReplaceAll) {
			replaced = strings.Count(content, payload.Old)
			updated = strings.ReplaceAll(content, payload.Old, payload.New)
		} else {
			updated = strings.Replace(content, payload.Old, payload.New, 1)
		}
		if err := os.WriteFile(path, []byte(updated), 0o644); err != nil {
			return "", err
		}
		return fmt.Sprintf("updated %s (%d replacement(s))", filepath.ToSlash(payload.Path), replaced), nil
	}
}

func globTool(workingDir string) func(ctx context.Context, rawArgs []byte) (string, error) {
	type args struct {
		Pattern string `json:"pattern"`
	}
	return func(ctx context.Context, rawArgs []byte) (string, error) {
		// Block glob when the edit deadline is approaching and no edits have been made.
		if e := GetEditEnforcer(ctx); e != nil && e.ShouldBlockReads() {
			logging.InfoContext(ctx, "blocking Glob call — edit deadline approaching, no edits made yet")
			return "GLOB BLOCKED — Edit deadline approaching with no edits. Call Edit or Write first.", nil
		}

		var payload args
		if err := json.Unmarshal(rawArgs, &payload); err != nil {
			return "", fmt.Errorf("invalid args: %w", err)
		}
		pattern := strings.TrimSpace(payload.Pattern)
		if pattern == "" {
			return "", fmt.Errorf("pattern is required")
		}
		matcher, err := newGlobMatcher(pattern)
		if err != nil {
			return "", err
		}
		var matches []string
		err = filepath.WalkDir(workingDir, func(path string, d os.DirEntry, err error) error {
			if err != nil {
				return err
			}
			if d.IsDir() {
				if defaultSkipDirs[d.Name()] {
					return filepath.SkipDir
				}
				return nil
			}
			rel, err := filepath.Rel(workingDir, path)
			if err != nil {
				return nil
			}
			rel = filepath.ToSlash(rel)
			if matcher.Match(rel) {
				matches = append(matches, rel)
			}
			return nil
		})
		if err != nil {
			return "", err
		}
		sort.Strings(matches)
		if len(matches) == 0 {
			return "no matches", nil
		}
		total := len(matches)
		if total > maxSearchResults {
			matches = matches[:maxSearchResults]
			result := fmt.Sprintf("[showing %d of %d matches — results truncated]\n%s", maxSearchResults, total, strings.Join(matches, "\n"))
			return TruncateToolOutputHeadTail(result, maxToolResultBytes), nil
		}
		result := strings.Join(matches, "\n")
		return TruncateToolOutputHeadTail(result, maxToolResultBytes), nil
	}
}

func grepTool(workingDir string) func(ctx context.Context, rawArgs []byte) (string, error) {
	type args struct {
		Pattern string `json:"pattern"`
		Path    string `json:"path"`
	}
	return func(ctx context.Context, rawArgs []byte) (string, error) {
		// Block grep when the edit deadline is approaching and no edits have been made.
		if e := GetEditEnforcer(ctx); e != nil && e.ShouldBlockReads() {
			logging.InfoContext(ctx, "blocking Grep call — edit deadline approaching, no edits made yet")
			return "GREP BLOCKED — Edit deadline approaching with no edits. Call Edit or Write first.", nil
		}

		var payload args
		if err := json.Unmarshal(rawArgs, &payload); err != nil {
			return "", fmt.Errorf("invalid args: %w", err)
		}
		pattern := strings.TrimSpace(payload.Pattern)
		if pattern == "" {
			return "", fmt.Errorf("pattern is required")
		}
		pattern = sanitizeRegex(pattern)
		re, err := regexp.Compile(pattern)
		if err != nil {
			return "", fmt.Errorf("invalid regex: %w", err)
		}
		searchPath := payload.Path
		if strings.TrimSpace(searchPath) == "" {
			searchPath = "."
		}
		resolved, err := ResolvePath(workingDir, searchPath)
		if err != nil {
			return "", err
		}

		matches, err := grepSearchPath(workingDir, resolved, re)
		if err != nil {
			return "", err
		}

		if len(matches) == 0 {
			return "no matches", nil
		}
		total := len(matches)
		if total > maxSearchResults {
			matches = matches[:maxSearchResults]
			result := fmt.Sprintf("[showing %d of %d grep matches — results truncated]\n%s", maxSearchResults, total, strings.Join(matches, "\n"))
			return TruncateToolOutputHeadTail(result, maxToolResultBytes), nil
		}
		result := strings.Join(matches, "\n")
		return TruncateToolOutputHeadTail(result, maxToolResultBytes), nil
	}
}

func grepSearchPath(workingDir, resolved string, re *regexp.Regexp) ([]string, error) {
	var matches []string
	visit := grepVisitFile(workingDir, re, &matches)

	info, err := os.Stat(resolved)
	if err != nil {
		return nil, err
	}
	if info.IsDir() {
		if err := filepath.Walk(resolved, visit); err != nil {
			return nil, err
		}
	} else {
		if err := visit(resolved, info, nil); err != nil {
			return nil, err
		}
	}
	return matches, nil
}

func grepVisitFile(workingDir string, re *regexp.Regexp, matches *[]string) filepath.WalkFunc {
	return func(path string, info os.FileInfo, walkErr error) (retErr error) {
		if walkErr != nil {
			return walkErr
		}
		if info.IsDir() {
			if defaultSkipDirs[info.Name()] {
				return filepath.SkipDir
			}
			return nil
		}
		file, err := os.Open(path)
		if err != nil {
			return nil
		}
		defer func() {
			if cerr := file.Close(); cerr != nil && retErr == nil {
				retErr = cerr
			}
		}()
		rel, err := filepath.Rel(workingDir, path)
		if err != nil {
			rel = path
		}
		scanner := bufio.NewScanner(file)
		scanner.Buffer(make([]byte, 64*1024), maxToolOutput)
		lineNum := 1
		for scanner.Scan() {
			line := scanner.Text()
			if re.MatchString(line) {
				*matches = append(*matches, fmt.Sprintf("%s:%d:%s", filepath.ToSlash(rel), lineNum, line))
			}
			lineNum++
		}
		if err := scanner.Err(); err != nil {
			return err
		}
		return nil
	}
}

func bashTool(ex executor.Executor) func(ctx context.Context, rawArgs []byte) (string, error) {
	type args struct {
		Command string `json:"command"`
	}
	return func(ctx context.Context, rawArgs []byte) (string, error) {
		var payload args
		if err := json.Unmarshal(rawArgs, &payload); err != nil {
			return "", fmt.Errorf("invalid args: %w", err)
		}
		command := strings.TrimSpace(payload.Command)
		if command == "" {
			return "", fmt.Errorf("command is required")
		}

		if blocked, reason := IsBlockedCommand(command); blocked {
			return "", fmt.Errorf("%s", reason)
		}

		// Add executor metadata to the current span so traces show
		// what command ran and which executor backend handled it.
		span := trace.SpanFromContext(ctx)
		span.SetAttributes(
			attribute.String("squad.executor.type", ex.Type()),
			attribute.String("squad.tool.command", TruncateString(command, 1000)),
		)

		output, err := ex.Execute(ctx, command)
		if err != nil {
			return TruncateToolOutputHeadTail(string(limitOutput(output)), maxToolResultBytes), fmt.Errorf("command failed: %w", err)
		}
		return TruncateToolOutputHeadTail(string(limitOutput(output)), maxToolResultBytes), nil
	}
}

// tokenCalibrationKeyType is the context key for TokenCalibration.
type tokenCalibrationKeyType struct{}

// InitTokenCalibration attaches a TokenCalibration to the context.
func InitTokenCalibration(ctx context.Context) context.Context {
	return context.WithValue(ctx, tokenCalibrationKeyType{}, NewTokenCalibration())
}

// GetTokenCalibration retrieves the TokenCalibration from context, or nil.
func GetTokenCalibration(ctx context.Context) *TokenCalibration {
	if tc, ok := ctx.Value(tokenCalibrationKeyType{}).(*TokenCalibration); ok {
		return tc
	}
	return nil
}

func estimateTokens(messages []llms.MessageContent) int {
	total := 0
	for _, msg := range messages {
		for _, part := range msg.Parts {
			switch p := part.(type) {
			case llms.TextContent:
				total += len(p.Text) / 4
			case llms.ToolCallResponse:
				total += len(p.Content) / 4
			}
		}
	}
	return total
}

// estimateTokensCalibrated uses token calibration data if available.
func estimateTokensCalibrated(ctx context.Context, messages []llms.MessageContent) int {
	raw := estimateTokens(messages)
	if tc := GetTokenCalibration(ctx); tc != nil && tc.Samples() >= 2 {
		return tc.CalibratedEstimate(raw)
	}
	return raw
}

func compactMessages(ctx context.Context, messages []llms.MessageContent) []llms.MessageContent {
	tokens := estimateTokensCalibrated(ctx, messages)
	if tokens < contextTokenThreshold {
		return messages
	}

	protectedHead := 2
	if protectedHead > len(messages) {
		return messages
	}
	protectedTail := keepRecentMessages
	if protectedTail > len(messages)-protectedHead {
		protectedTail = len(messages) - protectedHead
	}
	compactEnd := len(messages) - protectedTail

	// Build a structured summary of what happened before compaction,
	// so the agent retains awareness of files read, patterns searched, etc.
	middleMessages := messages[protectedHead:compactEnd]
	summary := CompactionSummary(middleMessages)

	compacted := make([]llms.MessageContent, len(messages))
	copy(compacted, messages)

	for i := protectedHead; i < compactEnd; i++ {
		if compacted[i].Role != llms.ChatMessageTypeTool {
			continue
		}
		newParts := make([]llms.ContentPart, len(compacted[i].Parts))
		for j, part := range compacted[i].Parts {
			if resp, ok := part.(llms.ToolCallResponse); ok {
				origLen := len(resp.Content)
				if origLen > 200 {
					resp.Content = fmt.Sprintf("[tool output compacted — was %d bytes]", origLen)
				}
				newParts[j] = resp
			} else {
				newParts[j] = part
			}
		}
		compacted[i].Parts = newParts
	}

	// Inject the session state summary as a system message right after the
	// protected head, so the agent knows what it has already done.
	if summary != "" {
		summaryMsg := llms.MessageContent{
			Role:  llms.ChatMessageTypeHuman,
			Parts: []llms.ContentPart{llms.TextPart(summary)},
		}
		// Insert after protected head messages.
		result := make([]llms.MessageContent, 0, len(compacted)+1)
		result = append(result, compacted[:protectedHead]...)
		result = append(result, summaryMsg)
		result = append(result, compacted[protectedHead:]...)
		compacted = result
	}

	after := estimateTokens(compacted)
	logging.InfoContext(ctx, "compacted context: ~%d tokens -> ~%d tokens", tokens, after)
	return compacted
}

func TruncateToolOutputHeadTail(output string, maxBytes int) string {
	if len(output) <= maxBytes {
		return output
	}

	headSize := maxBytes * 3 / 4
	tailSize := maxBytes - headSize

	head := output[:headSize]
	tail := output[len(output)-tailSize:]

	// Snap to line boundaries
	headEnd := strings.LastIndex(head, "\n")
	if headEnd < 0 {
		headEnd = len(head)
	}
	tailStart := strings.Index(tail, "\n")
	if tailStart < 0 {
		tailStart = 0
	} else {
		tailStart++
	}

	totalLines := strings.Count(output, "\n") + 1
	headLines := strings.Count(head[:headEnd], "\n") + 1
	tailLines := strings.Count(tail[tailStart:], "\n") + 1
	omitted := totalLines - headLines - tailLines
	if omitted < 0 {
		omitted = 0
	}

	return fmt.Sprintf("%s\n\n... [%d lines omitted from tool output — total %d bytes] ...\n\n%s",
		head[:headEnd], omitted, len(output), tail[tailStart:])
}

// FlexBool unmarshals both JSON booleans and string representations
// ("true"/"false") that LLMs sometimes produce for boolean fields.
type FlexBool bool

func (b *FlexBool) UnmarshalJSON(data []byte) error {
	var v bool
	if err := json.Unmarshal(data, &v); err == nil {
		*b = FlexBool(v)
		return nil
	}
	var s string
	if err := json.Unmarshal(data, &s); err != nil {
		return fmt.Errorf("replace_all must be bool or string, got %s", string(data))
	}
	switch strings.ToLower(strings.TrimSpace(s)) {
	case "true", "1", "yes":
		*b = true
	default:
		*b = false
	}
	return nil
}

func ResolvePath(workingDir, input string) (string, error) {
	if strings.TrimSpace(input) == "" {
		return "", fmt.Errorf("path is required")
	}
	var joined string
	if filepath.IsAbs(input) {
		joined = filepath.Clean(input)
	} else {
		joined = filepath.Join(workingDir, input)
	}
	abs, err := filepath.Abs(joined)
	if err != nil {
		return "", err
	}
	wd, err := filepath.Abs(workingDir)
	if err != nil {
		return "", err
	}
	if abs != wd && !strings.HasPrefix(abs, wd+string(filepath.Separator)) {
		return "", fmt.Errorf("path %q is outside working directory", input)
	}
	return abs, nil
}

func limitOutput(data []byte) []byte {
	if len(data) <= maxToolOutput {
		return data
	}
	head := data[:maxToolOutput]
	return append(head, []byte("\n...output truncated\n")...)
}

type globMatcher struct {
	re *regexp.Regexp
}

func newGlobMatcher(pattern string) (*globMatcher, error) {
	normalized := filepath.ToSlash(pattern)
	regex, err := globToRegex(normalized)
	if err != nil {
		return nil, err
	}
	re, err := regexp.Compile(regex)
	if err != nil {
		return nil, err
	}
	return &globMatcher{re: re}, nil
}

func (g *globMatcher) Match(path string) bool {
	return g.re.MatchString(path)
}

func globToRegex(pattern string) (string, error) {
	var buf strings.Builder
	buf.WriteString("^")
	runes := []rune(pattern)
	for i := 0; i < len(runes); i++ {
		ch := runes[i]
		if ch == '*' {
			if i+1 < len(runes) && runes[i+1] == '*' {
				buf.WriteString(".*")
				i++
			} else {
				buf.WriteString(`[^/]*`)
			}
			continue
		}
		if ch == '?' {
			buf.WriteString(".")
			continue
		}
		if strings.ContainsRune(`.+()|[]{}^$\\`, ch) {
			buf.WriteString(`\`)
		}
		buf.WriteRune(ch)
	}
	buf.WriteString("$")
	return buf.String(), nil
}

func TruncateString(s string, maxLen int) string {
	if len(s) <= maxLen {
		return s
	}
	return s[:maxLen] + "..."
}
