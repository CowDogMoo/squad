// maxReadToolRepeat caps consecutive identical Read/Glob/Grep calls.
// Models sometimes re-read the same file despite cache hints; a hard cap
// forces them to move on.
const maxReadToolRepeat = 5

var highRepeatTools = map[string]bool{
	"Read": true,
	"Glob": true,
	"Grep": true,
}

// reInlineModifierGroup matches inline modifier groups like (?n), (?x),
// (?imn), etc. — any (?<letters>) or (?<letters>-<letters>) pattern.
var reInlineModifierGroup = regexp.MustCompile(`\(\?[a-zA-Z]+(?:-[a-zA-Z]+)?\)`)

// sanitizeRegex strips regex syntax that is unsupported by Go's RE2 engine
// but commonly generated by LLMs (e.g., .NET's (?n) explicit-capture flag,
// PCRE's (?x), etc.). RE2 supports (?i), (?m), (?s), (?U) and combinations.
func sanitizeRegex(pattern string) string {
	return reInlineModifierGroup.ReplaceAllStringFunc(pattern, func(match string) string {
		// Extract the flags portion: strip leading "(?" and trailing ")"
		inner := match[2 : len(match)-1]
		// Check each character — keep the group only if ALL flags are valid RE2 flags
		for _, c := range inner {
			if c != 'i' && c != 'm' && c != 's' && c != 'U' && c != '-' {
				return "" // contains unsupported flag, strip entire group
			}
		}
		return match // all flags valid, keep as-is
	})
}

