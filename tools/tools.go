func executeToolCalls(ctx context.Context, messages []llms.MessageContent, toolCalls []llms.ToolCall, handlers map[string]Handler) []llms.MessageContent {
	// Batch all tool results into a single message.  Anthropic requires every
	// tool_result to be in the same user message that follows the assistant
	// message containing the corresponding tool_use blocks.
	parts := make([]llms.ContentPart, len(toolCalls))

	// Determine if we can parallelize: only when there are multiple calls
	// and none of them are serial (state-mutating) tools.
	canParallelize := len(toolCalls) > 1
	if canParallelize {
		for _, tc := range toolCalls {
			if tc.FunctionCall != nil && serialTools[tc.FunctionCall.Name] {
				canParallelize = false
				break
			}
		}
	}

	if canParallelize {
		logging.InfoContext(ctx, "executing %d tool calls in parallel", len(toolCalls))
		var wg sync.WaitGroup
		wg.Add(len(toolCalls))
		for i, toolCall := range toolCalls {
			go func(idx int, tc llms.ToolCall) {
				defer wg.Done()
				parts[idx] = executeToolCall(ctx, tc, handlers)
			}(i, toolCall)
		}
		wg.Wait()
	} else {
		for i, toolCall := range toolCalls {
			parts[i] = executeToolCall(ctx, toolCall, handlers)
		}
	}

	return append(messages, llms.MessageContent{
		Role:  llms.ChatMessageTypeTool,
		Parts: parts,
	})
}

func executeToolCall(ctx context.Context, toolCall llms.ToolCall, handlers map[string]Handler) llms.ToolCallResponse {
	toolResponse := llms.ToolCallResponse{
		ToolCallID: toolCall.ID,
	}
	if toolCall.FunctionCall == nil {
		toolResponse.Content = "tool call missing function definition"
		return toolResponse
	}

	handler, ok := handlers[toolCall.FunctionCall.Name]
	if !ok {
		toolResponse.Content = fmt.Sprintf("unknown tool: %s", toolCall.FunctionCall.Name)
		logging.DebugContext(ctx, "unknown tool requested: %s", toolCall.FunctionCall.Name)
		return toolResponse
	}

	toolName := toolCall.FunctionCall.Name
	ctx, span := telemetry.Tracer().Start(ctx, "tool."+toolName,
		trace.WithAttributes(
			attribute.String("squad.tool.name", toolName),
		),
	)
	defer span.End()

	toolResponse.Name = toolName
	argsSummary := toolArgsSummary(toolName, toolCall.FunctionCall.Arguments)
	logging.InfoContext(ctx, "  → %s %s", toolName, argsSummary)
	logging.DebugContext(ctx, "tool %s full args: %s", toolName, TruncateString(toolCall.FunctionCall.Arguments, 500))
	toolStart := time.Now()
	output, err := handler.Call(ctx, []byte(toolCall.FunctionCall.Arguments))
	toolDuration := time.Since(toolStart)
	if err != nil {
		span.RecordError(err)
		span.SetStatus(codes.Error, err.Error())
		// Include both output (e.g., command stdout/stderr) and error message
		if output != "" {
			toolResponse.Content = fmt.Sprintf("%s\n\nerror: %v", output, err)
		} else {
			toolResponse.Content = fmt.Sprintf("error: %v", err)
		}
		logging.InfoContext(ctx, "  ✗ %s failed in %s: %s", toolName, toolDuration.Round(time.Millisecond), TruncateString(err.Error(), 200))
	} else {
		toolResponse.Content = output
		logging.InfoContext(ctx, "  ✓ %s done in %s (%d bytes)", toolName, toolDuration.Round(time.Millisecond), len(output))
	}
	span.SetAttributes(attribute.Int("squad.tool.output_bytes", len(toolResponse.Content)))
	return toolResponse
}

// toolArgsSummary extracts a short, human-readable summary from tool call arguments.
// For Bash it shows the command, for Read/Write/Edit the path, for Grep the pattern, etc.
// argString extracts the first matching string value from a parsed JSON args map.
func argString(args map[string]interface{}, keys ...string) string {
	for _, k := range keys {
		if v, ok := args[k]; ok {
			if s, ok := v.(string); ok {
				return s
			}
		}
	}
	return ""
}

// simpleArgKeys maps tool names to the single arg key that summarizes them.
var simpleArgKeys = map[string]string{
	"Read":      "path",
	"Write":     "path",
	"Edit":      "path",
	"MultiEdit": "path",
	"Glob":      "pattern",
}

func toolArgsSummary(toolName, argsJSON string) string {
	var args map[string]interface{}
	if err := json.Unmarshal([]byte(argsJSON), &args); err != nil {
		return ""
	}
	if key, ok := simpleArgKeys[toolName]; ok {
		return argString(args, key)
	}
	return toolArgsSummaryComplex(toolName, args)
}

func toolArgsSummaryComplex(toolName string, args map[string]interface{}) string {
	switch toolName {
	case "Bash":
		if cmd := argString(args, "command"); cmd != "" {
			return TruncateString(cmd, 120)
		}
	case "Grep":
		p := argString(args, "pattern")
		if path := argString(args, "path"); path != "" {
			return p + " in " + path
		}
		return p
	case "Task":
		agent := argString(args, "agent")
		prompt := TruncateString(argString(args, "prompt"), 80)
		if agent != "" {
			return fmt.Sprintf("agent=%s prompt=%q", agent, prompt)
		}
	case "TaskResult":
		return "id=" + argString(args, "id")
	}
	return ""
}

func BuildHandlers(workingDir string, taskCfg *TaskConfig, ex executor.Executor) (map[string]Handler, []llms.Tool) {
	handlers := map[string]Handler{}

	add := func(handler Handler) {
		name := handler.Def.Function.Name
		handlers[name] = handler
	}

	add(Handler{Def: definitionRead(), Call: readTool(workingDir)})
	add(Handler{Def: definitionWrite(), Call: trackEdits(writeTool(workingDir))})
	add(Handler{Def: definitionEdit(), Call: trackEdits(editTool(workingDir))})
	add(Handler{Def: definitionGlob(), Call: globTool(workingDir)})
	add(Handler{Def: definitionGrep(), Call: grepTool(workingDir)})
	add(Handler{Def: definitionBash(), Call: bashTool(ex)})
	add(Handler{Def: definitionBashBg(), Call: bashBackgroundTool(ex)})
	add(Handler{Def: definitionBashOutput(), Call: bashOutputTool()})
	add(Handler{Def: definitionMultiEdit(), Call: trackEdits(multiEditTool(workingDir))})
	add(Handler{Def: definitionSystemInfo(), Call: systemInfoTool(ex)})
	add(Handler{Def: definitionRepoMap(), Call: repoMapTool(workingDir)})

	if taskCfg != nil {
		// Only register Task/TaskResult when the agent is allowed to spawn children.
		// DisableTask nils CallModel while keeping taskCfg alive for ExtraTools.
		if taskCfg.CallModel != nil {
			add(Handler{Def: definitionTask(), Call: taskTool(*taskCfg)})
			if taskCfg.Registry != nil {
				add(Handler{Def: definitionTaskResult(), Call: taskResultTool(*taskCfg)})
			}
		}
		if taskCfg.Findings != nil {
			agentName := taskCfg.AgentName
			if agentName == "" {
				agentName = "unknown"
			}
			add(Handler{Def: definitionReportFinding(), Call: reportFindingTool(taskCfg.Findings, agentName)})
		}
		// Merge additional tools from external providers (e.g., MCP servers).
		for _, h := range taskCfg.ExtraTools {
			add(h)
		}
	}

	toolDefs := make([]llms.Tool, 0, len(handlers))
	for _, handler := range handlers {
		toolDefs = append(toolDefs, handler.Def)
	}
	sort.Slice(toolDefs, func(i, j int) bool {
		return toolDefs[i].Function.Name < toolDefs[j].Function.Name
	})

	return handlers, toolDefs
}

func trackEdits(call func(ctx context.Context, rawArgs []byte) (string, error)) func(ctx context.Context, rawArgs []byte) (string, error) {
	return func(ctx context.Context, rawArgs []byte) (string, error) {
		result, err := call(ctx, rawArgs)
		if err == nil {
			MarkEditsApplied(ctx)
		}
		return result, err
	}
}

